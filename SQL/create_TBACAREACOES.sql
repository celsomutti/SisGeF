CREATE TABLE `tbacareacoes` (
  `SEQ_ACAREACAO` int(11) NOT NULL,
  `ID_ACAREACAO` varchar(100) NOT NULL,
  `DAT_ACAREACAO` datetime NOT NULL,
  `NUM_NOSSONUMERO` varchar(14) NOT NULL,
  `COD_ENTREGADOR` int(11) NOT NULL,
  `COD_BASE` int(11) NOT NULL,
  `DES_MOTIVO` varchar(60) NOT NULL,
  `DES_TRATATIVA` varchar(60) NOT NULL,
  `DES_APURACAO` varchar(60) DEFAULT NULL,
  `DES_RESULTADO` varchar(60) DEFAULT NULL,
  `VAL_EXTRAVIO` double DEFAULT '0',
  `VAL_MULTA` double DEFAULT '0',
  `DES_ENVIO_CORRESPONDENCIA` varchar(20) DEFAULT NULL,
  `DES_RETORNO_CORRESPONDENCIA` varchar(70) DEFAULT NULL,
  `DES_OBSERVACOES` mediumtext,
  `DES_EXECUTOR` varchar(20) DEFAULT 'SISTEMA',
  `DAT_MANUTENCAO` datetime DEFAULT NULL,
  PRIMARY KEY (`SEQ_ACAREACAO`),
  KEY `TBACAREACAO_IDX1` (`ID_ACAREACAO`),
  KEY `TBACAREACAO_IDX2` (`DAT_ACAREACAO`),
  KEY `TBACAREACAO_IDX3` (`NUM_NOSSONUMERO`),
  KEY `TBACAREACAO_IDX4` (`COD_ENTREGADOR`),
  KEY `TBACAREACAO_IDX5` (`COD_BASE`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
