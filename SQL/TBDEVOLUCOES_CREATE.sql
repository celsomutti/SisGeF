CREATE TABLE `TBDEVOLUCOES` (
  `SEQ_DEVOLUCAO` int(11) NOT NULL,
  `SEQ_OBJETO` int(11) NOT NULL,
  `DAT_EXPEDICAO` date DEFAULT NULL,
  `NUM_LACRE` int(11) DEFAULT '0',
  `NUM_NOSSONUMERO` varchar(11) DEFAULT '',
  `QTD_VOLUMES` int(11) DEFAULT '1',
  `COD_AGENTE` int(11) DEFAULT '0',
  `NOM_MOTORISTA` varchar(70) DEFAULT '',
  `DAT_RETIRADA` timestamp NULL DEFAULT NULL,
  `NOM_RECEBEDOR` varchar(70) DEFAULT '',
  `DAT_RECEPCAO` timestamp NULL DEFAULT NULL,
  `NOM_CONFERENTE` varchar(70) DEFAULT '',
  `DAT_CONFERENCIA` timestamp NULL DEFAULT NULL,
  `QTD_PESO_CONTAINER` float DEFAULT '0',
  `COD_STATUS_CONTAINER` int(11) DEFAULT '0',
  `COD_STATUS_OBJETO` int(11) DEFAULT '0',
  `NOM_USUARIO` varchar(20) DEFAULT 'SISTEMA',
  `DAT_MANUTENCAO` timestamp NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`SEQ_DEVOLUCAO`,`SEQ_OBJETO`),
  KEY `TBDEVOLUCOES_IX_1` (`DAT_EXPEDICAO`),
  KEY `TBDEVOLUCOES_IX_2` (`NUM_LACRE`),
  KEY `TBDEVOLUCOES_IX_3` (`NUM_NOSSONUMERO`),
  KEY `TBDEVOLUCOES_IX_4` (`COD_AGENTE`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
